export function BadgeIcon({ size = 24, type, ...props }: IconProps & { type: string }) {
  const badges: Record<string, { color: string; icon: string }> = {
    first_post: { color: '#10b981', icon: '✨' },
    '10_likes': { color: '#f59e0b', icon: '⭐' },
    channel_creator: { color: '#8b5cf6', icon: '🏆' },
    active_member: { color: '#3b82f6', icon: '🔥' },
  };

  const badge = badges[type] || { color: '#6b7280', icon: '🎖️' };

  return (
    <span
      style={{
        display: 'inline-flex',
        alignItems: 'center',
        justifyContent: 'center',
        width: size,
        height: size,
        borderRadius: '50%',
        backgroundColor: badge.color + '20',
        border: `2px solid ${badge.color}`,
        fontSize: size * 0.5,
      }}
      title={type.replace('_', ' ')}
      {...props}
    >
      {badge.icon}
    </span>
  );
}
